    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String dniO=txtDniO.getText();
        String nombreO=txtNombreO.getText(); 
        String nombreE=txtNombreE.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("YYYY/MM/dd");
        String FechaE= sdf.format(txtCalendario.getDate()); 
        String fechaIns=fecha(); 
        int precio=Integer.parseInt(txtPrecio.getText());
        
        try {
            if(e1.FechaR(FechaE)==1){
                JOptionPane.showMessageDialog(null, "YA EXISTE UNA PROGRAMACI√ìN CON ESTA FECHA");
            }else{
                if(e1.Busqueda(txtDniO.getText())==1){
                    double descuento=precio * 0.15;
                    double precioD=precio- descuento;
                    e1.registrar(dniO, nombreO, nombreE,FechaE, fechaIns, precioD);
                    Limpiar();
                    JOptionPane.showMessageDialog(null, "EL USUARIO ES SOCIO: -15%");
                }else{
                    e1.registrar(dniO, nombreO, nombreE,FechaE, fechaIns, precio);
                    Limpiar();
                }
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed


